testString = "Hello DevOps!"
print(testString)  # prints complete string
print(testString[0])  # Prints first character of the string
print(testString[0:5])  # Prints characters starting 0th to 5th
print(testString[6:12])  # Prints characters starting from 6th to 12th
print(testString[3:])  # Prints string starting from 3rd character
print(testString*3)  # Prints string three times
print(testString+"Hola")  # Prints concatenated string
