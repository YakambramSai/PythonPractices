tuple1 = ('ansible', 1111, 'puppet', 86.89, 'Git', 'aws')
tuple2 = ('miner', 'body')
print(tuple1)  # Prints complete tuple
print(tuple1[0])  # prints first element of the tuple
print(tuple1[1:3])  # prints elements starting from 2nd till 3rd
print(tuple1[2:])  # prints elements starting from 2nd
print(tuple1*3) # prints tuple1 three times
print(tuple1+tuple2)
